<script type="text/javascript">
    const showAcceptStockModal = "#showAcceptStockModal";

    function openAcceptStockModal(id = null) {

        loading('show');

        $("#assign_id").val(id);
        $(showAcceptStockModal).modal('show');

        var user_id = {{ $employee->user->id }};

        let url = '';
        if (id == null) {
            url = BASE_URL + '/ams/stock-assign/accept-all/' + user_id;
        } else {
            url = BASE_URL + '/ams/stock-assign/' + id + '/accept';
        }

        $("#submitBtn").click(function(){
            axios.post(url)
            .then(response => {
                notify(response.data.message, 'success');
                setTimeout(function () {
                    location.reload();
                }, 2600);

            })
            .catch(error => {
                const response = error.response;
                if (response){
                    notify(response.data.message, 'error');
                }
            })
            .finally(() => {
                loading('hide');
            });

        });
        loading('hide');
    }
</script>
